/**
 * Builds a mailto link for strategic interaction requests.
 * @param type The type of inquiry (e.g., 'Question', 'Advice')
 * @param subject The subject line of the inquiry
 */
export const buildInteractionRequest = (type: string, subject: string) => {
  const mailSubject = encodeURIComponent(`[ACKLO PROTOCOL] ${type}: ${subject}`);
  const mailBody = encodeURIComponent(`Institutional Inquiry Details:\n\nType: ${type}\nSubject: ${subject}\n\nStrategic Counsel Content:\n`);
  return `mailto:${CONTACT_EMAIL}?subject=${mailSubject}&body=${mailBody}`;
};

